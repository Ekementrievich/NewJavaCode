package helloWorld;

public class loopExample {

	public static void main(String[] args) {
		// using a for loop
		for(int i = 1; i <= 5; i++) {
			System.out.println("Iteration " + i);
		}
		
		// Using a while loop
		int count = 1;
		while (count <= 5) {
			System.out.println("Count: "+ count);
			count++;
		}
	}

}
